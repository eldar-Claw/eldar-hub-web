def json_to_typescript(news_data, market_data):
    now = datetime.now(IST)
    date_str = now.strftime("%Y-%m-%d %H:%M")
    hebrew_date = format_hebrew_date(now)
    time_str = now.strftime("%H:%M")
    update_str = now.strftime("%d.%-m.%Y %H:%M")
    
    header = TEMPLATE_HEADER.format(date_str=date_str)
    lines = [header]
    
    # Constants
    lines.append(f'export const NEWS_DATE = "{hebrew_date} — {time_str}";')
    lines.append(f'export const LAST_UPDATED = "{update_str}";')
    lines.append("")
    
    # REPORT
    report = news_data.get("report", {})
    breaking_items = report.get("breakingItems", [])[:5]
    breaking = ",\n    ".join(f'"{to_ts_string(b)}"' for b in breaking_items)
    lines.append(f"""export const REPORT = {{
  date: NEWS_DATE,
  breakingItems: [
    {breaking}
  ],
  executiveSummary: "{to_ts_string(report.get('executiveSummary',''))}",
  conclusion: "{to_ts_string(report.get('conclusion',''))}",
  watchNext24h: "{to_ts_string(report.get('watchNext24h',''))}"
}};""")
    lines.append("")
    
    # NEWS_ITEMS
    items_ts = ",\n".join(build_news_item_ts(i) for i in news_data.get("news_items", [])[:15])
    lines.append(f"export const NEWS_ITEMS: NewsItem[] = [\n{items_ts}\n];")
    lines.append("")
    
    # CONTENT_ITEMS
    content_ts = ",\n".join(build_news_item_ts(i) for i in news_data.get("content_items", [])[:5])
    lines.append(f"export const CONTENT_ITEMS: NewsItem[] = [\n{content_ts}\n];")
    lines.append("")
    
    # TRENDS
    trends = news_data.get("trends", [])[:3]
    trends_ts = ",\n".join(
        f'  {{ title: "{to_ts_string(t["title"])}", description: "{to_ts_string(t["description"])}", direction: "{t.get("direction","stable")}" }}'
        for t in trends
    )
    lines.append(f"export const TRENDS: Trend[] = [\n{trends_ts}\n];")
    lines.append("")
    
    # WINE_NEWS
    wine_ts = ",\n".join(build_news_item_ts(i) for i in market_data.get("wine_news", [])[:5])
    lines.append(f"export const WINE_NEWS: NewsItem[] = [\n{wine_ts}\n];")
    lines.append("")
    
    # TOURISM_NEWS
    tourism_ts = ",\n".join(build_news_item_ts(i) for i in market_data.get("tourism_news", [])[:5])
    lines.append(f"export const TOURISM_NEWS: NewsItem[] = [\n{tourism_ts}\n];")
    lines.append("")
    
    # MARKET_DATA
    md = market_data.get("market_data", {})
    
    def fmt_idx(items):
        return ",\n".join(
            f'    {{ name: "{to_ts_string(i["name"])}", value: "{i["value"]}", change: "{i["change"]}", direction: "{i.get("direction","stable")}" }}'
            for i in items
        )
    
    ihl = ", ".join(f'"{to_ts_string(h)}"' for h in md.get("israelHeadlines", []))
    wshl = ", ".join(f'"{to_ts_string(h)}"' for h in md.get("wallStreetHeadlines", []))
    
    lines.append(f"""export const MARKET_DATA: MarketData | null = {{
  date: NEWS_DATE,
  indices: [
{fmt_idx(md.get('indices', []))}
  ],
  currencies: [
{fmt_idx(md.get('currencies', []))}
  ],
  commodities: [
{fmt_idx(md.get('commodities', []))}
  ],
  israelHeadlines: [{ihl}],
  wallStreetHeadlines: [{wshl}],
  watchTomorrow: "{to_ts_string(md.get('watchTomorrow',''))}"
}};""")
    lines.append("")
    
    # INDUSTRY_NEWS
    industry = market_data.get("industry_news", [])
    ind_ts = ",\n".join(
        f'  {{ category: "{i["category"]}", title: "{to_ts_string(i["title"])}", summary: "{to_ts_string(i["summary"])}"' +
        (f', sourceUrl: "{i["sourceUrl"]}"' if i.get("sourceUrl") else '') + ' }'
        for i in industry
    )
    lines.append(f"export const INDUSTRY_NEWS: IndustryItem[] = [\n{ind_ts}\n];")
    lines.append("")
    
    return "\n".join(lines)


def push_to_github(content):
    now = datetime.now(IST)
    msg = f"EIH Update — {now.strftime('%d/%m/%Y %H:%M')} IST"
    headers = {"Authorization": f"token {GITHUB_TOKEN}", "Accept": "application/vnd.github.v3+json"}
    url = f"https://api.github.com/repos/{GITHUB_REPO}/contents/{GITHUB_FILE_PATH}"
    
    resp = requests.get(url, headers=headers, params={"ref": GITHUB_BRANCH}, timeout=30)
    sha = resp.json().get("sha") if resp.status_code == 200 else None
    
    encoded = base64.b64encode(content.encode("utf-8")).decode("utf-8")
    payload = {"message": msg, "content": encoded, "branch": GITHUB_BRANCH}
    if sha:
        payload["sha"] = sha
    
    resp = requests.put(url, headers=headers, json=payload, timeout=30)
    ok = resp.status_code in (200, 201)
    print(f"  GitHub: {'OK' if ok else 'FAIL'} ({resp.status_code})")
    return ok



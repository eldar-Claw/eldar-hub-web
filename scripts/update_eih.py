def format_hebrew_date(dt):
    day_map = {1: "יום שני", 2: "יום שלישי", 3: "יום רביעי",
               4: "יום חמישי", 5: "יום שישי", 6: "יום שבת", 7: "יום ראשון"}
    months = ["", "בינואר", "בפברואר", "במרץ", "באפריל", "במאי", "ביוני",
              "ביולי", "באוגוסט", "בספטמבר", "באוקטובר", "בנובמבר", "בדצמבר"]
    return f"{day_map[dt.isoweekday()]}, {dt.day} {months[dt.month]} {dt.year}"


def call_openai(system_prompt, user_prompt, max_tokens=8000):
    headers = {"Authorization": f"Bearer {OPENAI_API_KEY}", "Content-Type": "application/json"}
    models = ["gpt-4o-mini", "gpt-4.1-mini", "gpt-3.5-turbo"]
    
    for model in models:
        payload = {
            "model": model,
            "messages": [
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt},
            ],
            "max_tokens": max_tokens,
            "temperature": 0.7,
            "response_format": {"type": "json_object"},
        }
        for attempt in range(3):
            try:
                print(f"      Attempt {attempt+1}/3 with {model}...")
                resp = requests.post(f"{OPENAI_BASE_URL}/chat/completions",
                                   headers=headers, json=payload, timeout=120)
                if resp.status_code == 429:
                    time.sleep(10 * (attempt + 1))
                    continue
                resp.raise_for_status()
                return resp.json()["choices"][0]["message"]["content"].strip()
            except requests.exceptions.HTTPError:
                if resp.status_code == 429:
                    time.sleep(10 * (attempt + 1))
                    continue
                elif resp.status_code in (404, 400):
                    break
                if attempt < 2:
                    time.sleep(5)
                    continue
                raise
            except Exception:
                if attempt < 2:
                    time.sleep(5)
                    continue
                raise
    raise Exception("All models exhausted")


def clean_json(raw):
    """Clean GPT output to valid JSON."""
    # Remove markdown fences
    if "```" in raw:
        lines = raw.split("\n")
        cleaned = []
        inside = False
        for line in lines:
            if line.strip().startswith("```"):
                inside = not inside
                continue
            if inside or not raw.startswith("```"):
                cleaned.append(line)
        raw = "\n".join(cleaned) if cleaned else raw
    
    # Find the JSON object
    start = raw.find("{")
    end = raw.rfind("}") + 1
    if start >= 0 and end > start:
        raw = raw[start:end]
    
    # Fix common issues
    raw = raw.replace(",\n}", "\n}")
    raw = raw.replace(",\n]", "\n]")
    raw = re.sub(r',\s*}', '}', raw)
    raw = re.sub(r',\s*]', ']', raw)
    
    return raw


def sanitize(s):
    """Remove Hebrew abbreviations with internal quotes."""
    if not isinstance(s, str):
        return s
    abbrevs = {
        'ת"א': 'תל אביב', 'נאסד"ק': 'נאסדק', 'מנכ"ל': 'מנהל כללי',
        'ד"ר': 'דוקטור', 'סמנכ"ל': 'סמנהל כללי', 'בע"מ': 'בעמ',
        'עו"ד': 'עורך דין', 'רו"ח': 'רואה חשבון', 'יו"ר': 'יושב ראש',
        'ארה"ב': 'ארצות הברית', 'או"ם': 'האומות המאוחדות',
        'צה"ל': 'צהל', 'שב"כ': 'שבכ', 'מו"מ': 'משא ומתן', 'חו"ל': 'חול',
    }
    for a, r in abbrevs.items():
        s = s.replace(a, r)
    return s


def sanitize_deep(obj):
    if isinstance(obj, str):
        return sanitize(obj)
    elif isinstance(obj, list):
        return [sanitize_deep(x) for x in obj]
    elif isinstance(obj, dict):
        return {k: sanitize_deep(v) for k, v in obj.items()}
    return obj


def parse_json_safe(raw):
    """Try to parse JSON, with repair attempts."""
    raw = clean_json(raw)
    
    try:
        return json.loads(raw)
    except json.JSONDecodeError as e:
        print(f"      JSON error: {e}")
        # Try fixing truncated JSON by adding closing brackets
        for suffix in ["}", "]}", "]}}", "]}]}}"]:
            try:
                return json.loads(raw + suffix)
            except:
                continue
        raise


